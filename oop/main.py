class Vehicle:
    def __init__(self, make,color,owner,wheels,engine ):
        self.make = make
        self.color = color
        self.owner = owner
        self.wheels = wheels
        self.engine = engine

class House:
    def __init__(self, room,door,window,painting ):
        self.room = room
        self.door = door
        self.window = window
        self.painting = painting

class Animal:
    def __init__(self, name,weight,age,production ):
        self.name = name
        self.weight = weight
        self.age = age
        self.production = production

